package Bean;

/**
 *
 * @author khans
 */
public class ProductBean { 
    
    private String photo;
    private String category;
    private String description;
    private String initial_price;
    private String schedule_date;
    private int b_id;

    /**
     * @return the photo
     */
    public String getPhoto() {
        return photo;
    }

    /**
     * @param photo the photo to set
     */
    public void setPhoto(String photo) {
        this.photo = photo;
    }

    /**
     * @return the category
     */
    public String getCategory() {
        return category;
    }

    /**
     * @param category the category to set
     */
    public void setCategory(String category) {
        this.category = category;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the initial_price
     */
    public String getInitial_price() {
        return initial_price;
    }

    /**
     * @param initial_price the initial_price to set
     */
    public void setInitial_price(String initial_price) {
        this.initial_price = initial_price;
    }

    /**
     * @return the schedule_date
     */
    public String getSchedule_date() {
        return schedule_date;
    }

    /**
     * @param schedule_date the schedule_date to set
     */
    public void setSchedule_date(String schedule_date) {
        this.schedule_date = schedule_date;
    }

    /**
     * @return the b_id
     */
    public int getB_id() {
        return b_id;
    }

    /**
     * @param b_id the b_id to set
     */
    public void setB_id(int b_id) {
        this.b_id = b_id;
    }
    
    
    
}
